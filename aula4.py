"""
tipos int e float
int -> numero inteiro
o tipo int representa qualquer numero positivo ou negativo
int sem sinal é considerado positivo
"""
print(11)
print(-11)

"""
float -> numero com ponto flutuante
o tipo float representa qualquer numero posit. ou neg.
com ponto flutuante
float sem sinal é considerado positivo
"""
print(1.1, 10.11)
print(0.0, -1.5)
"""
a função type mostra o tipo que o Python inferiu o valor
"""
print(type('Lucca'))
print(type(1))
print(type(1.1), type(-1.1), type('ola'))